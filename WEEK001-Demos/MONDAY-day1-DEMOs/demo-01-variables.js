// DEMO 1: Variables and Data Types
// Use this for live coding demonstration

// VARIABLES - Like labeled boxes
let name = "Alice";
const age = 25;
let height = 5.6;
let isStudent = true;

console.log(`Hi, I'm ${name}. I'm ${age} years old.`);

// MATH OPERATIONS
let birthYear = 2026 - age;
console.log(`You were born in ${birthYear}`);

// More math examples
let x = 10;
let y = 3;

console.log(`\nMath Operations:`);
console.log(`Addition: ${x + y}`);       // 13
console.log(`Subtraction: ${x - y}`);    // 7
console.log(`Multiplication: ${x * y}`); // 30
console.log(`Division: ${x / y}`);       // 3.333...
console.log(`Remainder (Modulo): ${x % y}`);  // 1

// TEACHING POINTS:
// - let vs const (changeable vs constant)
// - Template literals use backticks and ${}
// - % (modulo) gives remainder
// - Useful for checking even: n % 2 === 0
